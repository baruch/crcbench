static void verify_crc32(test_func_t func) {
	struct crc32c_testvec *tv;

	for (tv = &crc32c_testvectors[0]; tv->psize != 0; tv++) {
		uint32_t crc = func(tv->plaintext, tv->psize, 0);
		printf("test-crc32c: %s: [actual=%08x, expected=%08x]\n",
				crc == tv->value ? "PASS" : "FAIL",
				crc, tv->value);
	}
}

