static void fill_buf(void *data, size_t data_size)
{
	int fd = open("/dev/urandom", O_RDONLY);
	ssize_t ret = read(fd, data, data_size);
	if (ret < (ssize_t)data_size) {
		printf("Read %d bytes, expected %u\n", (int)ret, (unsigned)data_size);
	}
	close(fd);
}

static void test_buf(test_func_t func, void *buf, size_t buf_size, uint64_t *result)
{
	volatile uint64_t begin, end;
	
	fill_buf(buf, buf_size);

	begin = rdtsc();
	func(buf, buf_size, 0);
	end = rdtsc();
	*result = end - begin;
}

static int result_cmp(const void *a, const void *b)
{
	const uint64_t *a1 = a, *b1 = b;
	return (int)(*a1 - *b1);
}

static void display_result(uint64_t *results, size_t num_iters, size_t buf_size, size_t align)
{
	int i;

	qsort(results, num_iters, sizeof(results[0]), result_cmp);

	/*
	printf("size=%8d/align=%d/ (cycles):      ", (int)buf_size, (int)align);
	for (i = 1; i <= 5; i++)
		printf(" %10"PRIu64, results[i*num_iters/6]);
	printf("\n");
	*/

	printf("size=%8d/align=%d/ (cycles/byte): ", (int)buf_size, (int)align);
	for (i = 1; i <= 5; i++)
		printf(" %10.3lg", ((double)results[i*num_iters/6])/ ((double)buf_size));
	printf("\n");
}

static void test(test_func_t func)
{
	size_t buf_size;
	const size_t max_buf_size = 1024*1024;
	const size_t num_iters = 50;
	size_t iter;
	size_t align;
	void *buf = malloc(max_buf_size + 8);
	uint64_t *results = calloc(num_iters, sizeof(uint64_t));

	for (buf_size = 64; buf_size <= max_buf_size; buf_size *= 2) {
		for (align = 0; align < 8; align++) {
			for (iter = 0; iter < num_iters; iter++) {
				test_buf(func, buf+align, buf_size, &results[iter]);
			}
			display_result(results, num_iters, buf_size, align);
		}
		printf("\n");
	}

	free(buf);
	free(results);
}


